//go:build ignore

package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"runtime"
	"strings"
)

func main() {
	_, filename, _, ok := runtime.Caller(0)
	if !ok {
		panic("failed to resolve generator path")
	}
	baseDir := filepath.Dir(filename)
	rootDir := filepath.Clean(filepath.Join(baseDir, "..", ".."))

	verbs, err := loadSQLVerbs(filepath.Join(baseDir, "sql_verbs.json"))
	if err != nil {
		panic(err)
	}

	goSource, err := renderGoSource(verbs)
	if err != nil {
		panic(err)
	}
	if err := os.WriteFile(filepath.Join(baseDir, "sql_verbs.gen.go"), goSource, 0o644); err != nil {
		panic(err)
	}

	jsSource := renderJSSource(verbs)
	if err := os.WriteFile(filepath.Join(rootDir, "jsh", "usr", "lib", "sql_verbs.js"), jsSource, 0o644); err != nil {
		panic(err)
	}
}

func loadSQLVerbs(path string) ([]string, error) {
	body, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var verbs []string
	if err := json.Unmarshal(body, &verbs); err != nil {
		return nil, err
	}
	seen := map[string]struct{}{}
	for i, verb := range verbs {
		verb = strings.ToUpper(strings.TrimSpace(verb))
		if verb == "" {
			return nil, fmt.Errorf("sql_verbs.json contains an empty verb at index %d", i)
		}
		if _, ok := seen[verb]; ok {
			return nil, fmt.Errorf("sql_verbs.json contains duplicate verb %q", verb)
		}
		seen[verb] = struct{}{}
		verbs[i] = verb
	}
	return verbs, nil
}

func renderGoSource(verbs []string) ([]byte, error) {
	buf := &bytes.Buffer{}
	buf.WriteString("// Code generated by go generate; DO NOT EDIT.\n\n")
	buf.WriteString("package session\n\n")
	buf.WriteString("var implicitSQLVerbs = map[string]struct{}{\n")
	for _, verb := range verbs {
		fmt.Fprintf(buf, "\t%q: {},\n", verb)
	}
	buf.WriteString("}\n")
	return format.Source(buf.Bytes())
}

func renderJSSource(verbs []string) []byte {
	buf := &bytes.Buffer{}
	buf.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	buf.WriteString("'use strict';\n\n")
	buf.WriteString("const SQL_VERBS = new Set([\n")
	for _, verb := range verbs {
		fmt.Fprintf(buf, "    %q,\n", verb)
	}
	buf.WriteString("]);\n\n")
	buf.WriteString("module.exports = {\n")
	buf.WriteString("    SQL_VERBS,\n")
	buf.WriteString("};\n")
	return buf.Bytes()
}
